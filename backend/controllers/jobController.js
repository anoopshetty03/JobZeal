// Controller to search for jobs based on query parameters
export const searchJobs = catchAsyncErrors(async (req, res, next) => {
  const { q, fixedSalary, salaryFrom, salaryTo } = req.query;
  const query = { expired: false };

  // Add search conditions to query
  if (q) {
    query.$or = [
      { title: { $regex: q, $options: "i" } },
      { category: { $regex: q, $options: "i" } },
      { city: { $regex: q, $options: "i" } },
    ];
  }

  if (fixedSalary) {
    query.fixedSalary = fixedSalary;
  }

  if (salaryFrom || salaryTo) {
    query.$or = query.$or || [];
    if (salaryFrom) {
      query.$or.push({ salaryFrom: { $gte: salaryFrom } });
    }
    if (salaryTo) {
      query.$or.push({ salaryTo: { $lte: salaryTo } });
    }
  }

  // Find jobs based on query
  try {
    const jobs = await Job.find(query);
    res.status(200).json({
      success: true,
      jobs,
    });
  } catch (error) {
    return next(new ErrorHandler("Failed to fetch jobs. Please try again.", 500));
  }
});

// Controller to get a single job by ID
export const getSingleJob = catchAsyncErrors(async (req, res, next) => {
  const { id } = req.params;
  try {
    const job = await Job.findById(id);
    if (!job) {
      return next(new ErrorHandler("Job not found.", 404));
    }
    res.status(200).json({
      success: true,
      job,
    });
  } catch (error) {
    return next(new ErrorHandler(`Invalid ID / CastError`, 404));
  }
});

